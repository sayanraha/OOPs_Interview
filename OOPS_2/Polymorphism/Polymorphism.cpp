// Polymorphism -> Poly means many and morph means forms -> many forms

/* 
Polymorphism in C++ is the ability of different classes to be treated as objects of a common parent class. It allows a single interface to represent different underlying forms (types). There are two types of polymorphism in C++:

    Compile-time Polymorphism (Static Polymorphism): Achieved through function overloading and operator overloading.
                                                     The decision on which function to call is made at compile time.


    Run-time Polymorphism (Dynamic Polymorphism): Achieved through function overriding and virtual functions.
                                                  The decision on which function to call is made at runtime.
*/
